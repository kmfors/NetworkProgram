/**
 * TCP断开连接的过程会发生一些意想不到的事情
 * 
 * 如果单方面断开连接使用close函数，即只关闭一部分数据交换中使用的流，会造成可以传输数据，
 * 但无法接收；或可以接收数据但无法传输。
 * 
 * 一旦两台主机建立了套接字连接，每个主机就会拥有单独的输入流与输出流。
 * 
 * 针对优雅断开的 shutdown 函数
 * int shutdown(int sock, int howto);
 *  - howto：传递断开方式信息
 *      - SHUT_RD 断开输入流，无法接收数据，输入缓冲区即使收到数据也会抹除掉
 *      - SHUT_WR 断开输出流，无法传输数据，输出缓冲留有的未传输数据，将继续传递
 *      - SHUT_RDWR 同时断开IO流，相当于前两次的调用
 * 
 * close函数在调用的同时，关闭IO流，也发送EOF，可这样无法再接收对方传输的数据。
 * 若调用shutdown函数，只关闭服务器的输出流（半关闭），既可以保留EOF，也保留输入流。
*/