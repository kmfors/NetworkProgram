/**
 * 水平触发像一位耐心的老师，会反复提醒你还有作业没做完；
 * 边缘触发像一位严格的教练，只在你开始做和停下时发一次指令，剩下的全靠你的自觉和效率。
 * 
 * 假设一个 socket 收到了 2KB 的数据。
 * 
 * 水平触发：
 *  - epoll_wait 返回，告知可读。你只读了 1KB。
 *  - 再次调用 epoll_wait，它会立即再次返回，告诉你还有数据可读。
 * 
 * 边缘触发：
 *  - epoll_wait 返回，告知可读。你只读了 1KB。
 *  - 再次调用 epoll_wait，它会阻塞，即使还有 1KB 数据在缓冲区里。
 *  - 因为状态没有发生新的变化。
 *  - 正确做法：在第一次收到通知后，在一个循环中不断读取，直到 read 返回 -1，并且 errno 被设置为 EAGAIN。
*/